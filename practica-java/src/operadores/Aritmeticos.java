package operadores;

public class Aritmeticos {
    static void main() {
        System.out.println("*** Operadores Aritmeticos ***");
        double a = 5, b = 3, resultado;
        //suma
        resultado = a + b;
        System.out.println("resultado = " + resultado);
        //resta
        resultado = a - b;
        System.out.println("resultado = " + resultado);
        //multiplicacion
        resultado = a * b;
        System.out.println("resultado = " + resultado);
        //division
        resultado = a / b;
        System.out.println("resultado = " + resultado);
        //modulo o residuo
        resultado = a % b;
        System.out.println("resultado = " + resultado);
    }
}
